// Convert function
function convert(boxNumber) {
    const input = document.getElementById(`input${boxNumber}`).value;
    const output = document.getElementById(`output${boxNumber}`);
    const selectedStyle = document.getElementById("unicodeStyle").value;

    const styleMap = unicodeStyles[selectedStyle] || {};
    let result = "";

    for (let char of input) {
        result += styleMap[char] || char;
    }

    output.value = result;
}

// Copy function
function copyText(boxNumber, btn) {
    const output = document.getElementById(`output${boxNumber}`);
    output.select();
    output.setSelectionRange(0, 99999); // For mobile devices
    document.execCommand("copy");

    btn.textContent = "Copied!";
    btn.disabled = true;
    setTimeout(() => {
        btn.textContent = "Copy";
        btn.disabled = false;
    }, 1200);
}
